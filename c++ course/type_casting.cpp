#include<iostream>
using namespace std;
int main(){
    //typecasting means changing the variable of one datatype into another.
    int k=65;
    float b=102.56;
    cout<<"the value of k:"<<k<<endl;
    cout<<"the value of k after typecasting into float is: "<<float(k)<<endl;
    cout<<"the value of b is:"<<b<<endl;
    cout<<"the value of b after typecasting into int is:"<<int(b)<<endl;
    cout<<"the value of k after typecasting into char is: "<<char(k)<<endl;
    cout<<"the value of b after typecasting into char is: "<<char(b)<<endl; 
return 0;
}